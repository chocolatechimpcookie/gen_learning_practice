-- sql is not case sensitive

-- select everything from customers table

SELECT * FROM table_name;

-- SELECT extracts data from a database
-- Update updates database
-- delete deletes database
-- INSERT INTO inserts new database
-- CREATE DATABASE creates a new DATABASE
-- ALTER TABLE modifies a TABLE
-- DROP TABLE deletes a TABLE
-- CREATE INDEX creates an index (search key)
-- DROP INDEX deletes an index

-- SELECT specific columns Syntax
SELECT column1, column2 FROM table_name;

-- select only distinct (different values. Columns can contain duplicate values )

SELECT DISTINCT column1, column2 FROM table_name;
